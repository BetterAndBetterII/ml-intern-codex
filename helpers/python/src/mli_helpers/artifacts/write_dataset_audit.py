"""Write a dataset-audit artifact bundle."""

from __future__ import annotations

import argparse
from collections import OrderedDict
import sys

from ._writer import ArtifactWriterError, default_report_json, print_result, write_artifact


KIND = "dataset_audit"


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--artifact-dir", required=True, help="Target artifact directory")
    parser.add_argument("--turn-id", help="Optional local turn id; autogenerated if omitted")
    parser.add_argument("--dataset", required=True)
    parser.add_argument("--split", action="append", default=[], dest="splits")
    parser.add_argument(
        "--row-count",
        action="append",
        default=[],
        dest="row_counts",
        metavar="SPLIT=COUNT",
        help="Repeat for each split row count",
    )
    parser.add_argument("--issue", action="append", default=[], dest="issues")
    parser.add_argument("--summary", help="Override manifest summary")
    parser.add_argument("--title", help="Override artifact title")
    parser.add_argument("--tag", action="append", default=[], dest="tags")
    parser.add_argument("--raw-text")
    return parser


def parse_row_counts(entries: list[str]) -> OrderedDict[str, int]:
    row_counts: OrderedDict[str, int] = OrderedDict()
    for entry in entries:
        if "=" not in entry:
            raise ArtifactWriterError(f"row count must look like split=count: {entry}")
        split, raw_count = entry.split("=", 1)
        split = split.strip()
        if not split:
            raise ArtifactWriterError("row count split name must be non-empty")
        try:
            count = int(raw_count)
        except ValueError as error:
            raise ArtifactWriterError(f"row count must be an integer: {entry}") from error
        row_counts[split] = count
    return row_counts


def render_markdown(dataset: str, splits: list[str], row_counts: OrderedDict[str, int], issues: list[str]) -> str:
    lines = [
        f"# Dataset Audit: {dataset}",
        "",
        "## Split Summary",
    ]
    if splits:
        for split in splits:
            count = row_counts.get(split)
            suffix = f" ({count} rows)" if count is not None else ""
            lines.append(f"- {split}{suffix}")
    else:
        lines.append("- No splits recorded")
    lines.extend(["", "## Issues"])
    if issues:
        lines.extend(f"- {issue}" for issue in issues)
    else:
        lines.append("- No issues recorded")
    return "\n".join(lines)


def main(argv: list[str] | None = None) -> int:
    parser = build_parser()
    args = parser.parse_args(argv)

    try:
        row_counts = parse_row_counts(args.row_counts)
        metadata = {
            "dataset": args.dataset,
            "splits": args.splits,
            "row_counts": row_counts,
            "issues": args.issues,
        }
        title = args.title or f"Dataset audit for {args.dataset}"
        issue_count = len(args.issues)
        split_count = len(args.splits)
        summary = args.summary or f"{split_count} splits, {issue_count} issues detected for {args.dataset}"
        report_markdown = render_markdown(args.dataset, args.splits, row_counts, args.issues)
        report_json = default_report_json(metadata)
        result = write_artifact(
            artifact_dir=args.artifact_dir,
            turn_id=args.turn_id,
            kind=KIND,
            title=title,
            summary=summary,
            metadata=metadata,
            report_markdown=report_markdown,
            report_json=report_json,
            raw_text=args.raw_text,
            tags=args.tags or ["dataset", "audit"],
        )
    except ArtifactWriterError as error:
        parser.exit(status=2, message=f"error: {error}\n")

    print_result(result)
    return 0


if __name__ == "__main__":
    sys.exit(main())
